    cout << "我来自中国北京市！" << endl;
    cout << "我喜欢编程!" << endl;
    cout << "我喜欢打篮球!" << endl;
    cout << "我喜欢看书!" << endl;
    cout << "我喜欢旅游!" << endl;
    cout << "我喜欢架子鼓!" << endl;
