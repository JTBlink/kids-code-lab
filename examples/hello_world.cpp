/**
 * 第一个C++程序 - Hello World
 * 适合：完全零基础的同学
 * 学习目标：了解C++程序的基本结构
 */

#include <iostream>
using namespace std;

int main() {
    // 输出欢迎信息
    cout << "Hello, World!" << endl;
    cout << "欢迎来到C++编程世界！" << endl;
    
    // 程序正常结束
    return 0;
}

/*
程序解释：
1. #include <iostream> - 包含输入输出流头文件
2. using namespace std - 使用标准命名空间
3. int main() - 主函数，程序从这里开始执行
4. cout - 用于输出内容到屏幕
5. endl - 换行符
6. return 0 - 表示程序正常结束
*/
